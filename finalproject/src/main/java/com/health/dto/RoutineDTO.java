package com.health.dto;


public class RoutineDTO {
	
	String pr_info; 
	int pr_num, et_num, pu_num, pr_time, pr_calorie;
	
	public String getPr_info() {
		return pr_info;
	}
	public void setPr_info(String pr_info) {
		this.pr_info = pr_info;
	}
	public int getPr_num() {
		return pr_num;
	}
	public void setPr_num(int pr_num) {
		this.pr_num = pr_num;
	}
	public int getEt_num() {
		return et_num;
	}
	public void setEt_num(int et_num) {
		this.et_num = et_num;
	}
	public int getPu_num() {
		return pu_num;
	}
	public void setPu_num(int pu_num) {
		this.pu_num = pu_num;
	}
	public int getPr_time() {
		return pr_time;
	}
	public void setPr_time(int pr_time) {
		this.pr_time = pr_time;
	}
	public int getPr_calorie() {
		return pr_calorie;
	}
	public void setPr_calorie(int pr_calorie) {
		this.pr_calorie = pr_calorie;
	} 
	

	
}
